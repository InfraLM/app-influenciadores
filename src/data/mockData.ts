export function getRanking(monthYear: string): RankingEntry[] {
  const performances = mockPerformance
    .filter(p => p.monthYear === monthYear)
    .sort((a, b) => b.totalScore - a.totalScore);

  return performances.map((p, index) => {
    const influencer = mockInfluencers.find(i => i.id === p.influencerId);
    return {
      position: index + 1,
      influencerId: p.influencerId,
      influencerName: influencer?.fullName || 'Desconhecido',
      instagram: influencer?.instagram,
      totalScore: p.totalScore,
      isTopThree: index < 3,
    };
  });
}
